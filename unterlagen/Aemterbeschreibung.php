<h2>Finanzprüfer</h2>
<div>
    Die Aufgabe der Finanzprüfung liegt darin sicherzustellen, dass mit den Vereinsgeldern ordnungsgemäß umgegangen
    wird. Um diese Aufgabe gewissenhaft durchführen zu können sind Kenntnisse in den Bereichen Rechnungswesen,
    Steuerrecht und Controlling notwendig. Es ist wünschenswert einen Ehrgeiz für Neutralität mitzubringen.
</div>

<h2>Schlichter</h2>
<div>
    Die Aufgabe der Schlichter ist es, Streitigkeiten im Rahmen des Vereinslebens zu entschärfen und bei der
    Lösungsfindung zu unterstützen. Alle Tätigkeiten der Schlichter sind streng vertraulich. Jedes Mitglied kann sich
    jeder Zeit an die Schlichter wenden. Das Arbeitsaufkommen ist stark schwankend, da es abhängig von der Zahl der
    Schlichtungsaufträge und der jeweiligen Konfliktschärfe ist. Die Themen sind so vielfaltig wie das Menschsein.
</div>


